const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
        if (entry.isIntersecting) {
            entry.target.classList.add("is-visible");
            revealObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.12 });

revealItems.forEach((item) => revealObserver.observe(item));

const counterObserver = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
        if (entry.isIntersecting) {
            animateCounter(entry.target);
            counterObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.8 });

counterItems.forEach((counter) => counterObserver.observe(counter));

const sectionObserver = new IntersectionObserver((entries) => {
    const visible = entries.filter((entry) => entry.isIntersecting).sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];
    if (visible?.target?.id) setActiveNav(visible.target.id);
}, { threshold: [0.2, 0.4, 0.65] });

document.querySelectorAll("section[id]").forEach((section) => sectionObserver.observe(section));
window.addEventListener("scroll", updateScrollState, { passive: true });

document.addEventListener("pointermove", (event) => {
    const x = event.clientX;
    const y = event.clientY;
    if (cursorGlow) cursorGlow.style.transform = `translate(${x - 220}px, ${y - 220}px)`;
    root.style.setProperty("--spotlight-x", `${(x / window.innerWidth) * 100}%`);
    root.style.setProperty("--spotlight-y", `${(y / window.innerHeight) * 100}%`);
});

magneticElements.forEach((element) => {
    element.addEventListener("pointermove", (event) => {
        const rect = element.getBoundingClientRect();
        const offsetX = ((event.clientX - rect.left) / rect.width - 0.5) * 10;
        const offsetY = ((event.clientY - rect.top) / rect.height - 0.5) * 10;
        element.style.transform = `translate(${offsetX}px, ${offsetY}px)`;
    });
    element.addEventListener("pointerleave", () => { element.style.transform = ""; });
});

tiltElements.forEach((element) => {
    element.addEventListener("pointermove", (event) => {
        const rect = element.getBoundingClientRect();
        const rotateY = ((event.clientX - rect.left) / rect.width - 0.5) * 10;
        const rotateX = ((event.clientY - rect.top) / rect.height - 0.5) * -10;
        element.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg)`;
    });
    element.addEventListener("pointerleave", () => { element.style.transform = ""; });
});

