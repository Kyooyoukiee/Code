function createTag(text) {
    const element = document.createElement("span");
    element.textContent = text;
    return element;
}

function renderProject(key) {
    const project = projects[key];
    if (!project) return;
    activeProject = key;
    projectTabs.forEach((tab) => tab.classList.toggle("is-active", tab.dataset.project === key));
    projectKicker.textContent = project.label;
    projectTitle.textContent = project.title;
    projectHeading.textContent = project.title;
    projectTagline.textContent = project.tagline;
    projectDescription.textContent = project.description;
    projectCategory.textContent = project.category;
    projectDeliverables.textContent = project.deliverables;
    projectTimeframe.textContent = project.timeframe;
    projectImpact.textContent = project.impact;
    projectStack.textContent = project.stack;
    projectMetricLabel.textContent = project.metricLabel;
    projectMetricValue.textContent = project.metricValue;
    projectImpactLabel.textContent = project.impactLabel;
    projectImpactValue.textContent = project.impactValue;
    previewStrip.innerHTML = "";
    projectHighlights.innerHTML = "";
    projectChart.innerHTML = "";
    project.highlights.forEach((item) => {
        previewStrip.appendChild(createTag(item));
        projectHighlights.appendChild(createTag(item));
    });
    project.chart.forEach((height) => {
        const bar = document.createElement("i");
        bar.style.setProperty("--height", height);
        projectChart.appendChild(bar);
    });
    previewSurface.style.setProperty("--project-accent", project.accent);
    previewSurface.style.setProperty("--project-glow", project.glow);
}

function openModal(key) {
    const project = projects[key];
    if (!project) return;
    modalCategory.textContent = project.label;
    modalTitle.textContent = project.title;
    modalNarrative.textContent = project.narrative;
    modalPoints.innerHTML = "";
    modalDeliverables.innerHTML = "";
    project.points.forEach((point) => {
        const item = document.createElement("li");
        item.textContent = point;
        modalPoints.appendChild(item);
    });
    project.deliverableList.forEach((item) => modalDeliverables.appendChild(createTag(item)));
    modal.hidden = false;
    body.classList.add("modal-open");
}

function closeModal() {
    modal.hidden = true;
    body.classList.remove("modal-open");
}

function animateCounter(element) {
    const target = Number(element.dataset.counter);
    const duration = 1300;
    const start = performance.now();
    function step(now) {
        const progress = Math.min((now - start) / duration, 1);
        element.textContent = Math.floor(progress * target);
        if (progress < 1) requestAnimationFrame(step);
        else element.textContent = target;
    }
    requestAnimationFrame(step);
}

function setActiveNav(sectionId) {
    navLinks.forEach((link) => link.classList.toggle("is-active", link.getAttribute("href") === `#${sectionId}`));
}

function updateScrollState() {
    const scrollTop = window.scrollY || window.pageYOffset;
    const maxScroll = Math.max(document.documentElement.scrollHeight - window.innerHeight, 1);
    const progress = Math.min((scrollTop / maxScroll) * 100, 100);
    body.classList.toggle("is-scrolled", scrollTop > 16);
    root.style.setProperty("--scroll-progress", `${progress}%`);
}

function renderTestimonial(index) {
    const item = testimonials[index];
    if (!item) return;
    activeTestimonial = index;
    quoteText.textContent = item.quote;
    quoteName.textContent = item.name;
    quoteRole.textContent = item.role;
    [...testimonialDots.children].forEach((dot, dotIndex) => dot.classList.toggle("is-active", dotIndex === index));
}

function startTestimonials() {
    clearInterval(testimonialInterval);
    testimonialInterval = window.setInterval(() => renderTestimonial((activeTestimonial + 1) % testimonials.length), 4800);
}

